package javaDayTwo.operators;

public class ArithmeticOperators {
	public static void main(String[] args) {
	    
	    // declare variables
	    int a = 13, b = 5;

	    System.out.println("a "+ a);
	    // addition operator
	    System.out.println("a + b = " + (a + b));

	    // subtraction operator
	    System.out.println("a - b = " + (a - b));

	    // multiplication operator
	    System.out.println("a * b = " + (a * b));

	    // division operator
	    System.out.println("a / b = " + (a / b));

	    // modulo operator
	    System.out.println("a % b = " + (a % b));
	  }

}
